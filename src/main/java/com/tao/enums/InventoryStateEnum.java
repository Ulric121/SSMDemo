package com.tao.enums;

/**
 * 使用枚举表述常量数据字典
 * 
 * @author Ulric
 * @version $Id: InventoryEnum.java, v 1.0 2017年4月1日 下午8:41:33 Ulric Exp $
 */
public enum InventoryStateEnum {
    SUCCESS(1,"秒杀成功"),
    END(0,"秒杀结束"),
    REPEAT_KILL(-1,"重复秒杀"),
    INNER_ERROR(-2,"系统异常"),
    DATA_REWRITE(-3,"数据篡改");

    private int    state;

    private String stateInfo;

    private InventoryStateEnum(int state, String stateInfo) {
        this.state = state;
        this.stateInfo = stateInfo;
    }

    public int getState() {
        return state;
    }

    public String getStateInfo() {
        return stateInfo;
    }

    /**
     * 根据state返回枚举类型
     * @param index
     * @return
     */
    public static InventoryStateEnum stateOf(int index) {
        for (InventoryStateEnum state : values()) {
            if (state.getState() == index) {
                return state;
            }
        }
        return null;
    }

}
